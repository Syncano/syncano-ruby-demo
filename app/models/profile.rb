class Profile < ActiveRecord::Base
  # ----------------------------------------------------------------------
  # Associations
  # ----------------------------------------------------------------------
  belongs_to :user

  # ----------------------------------------------------------------------
  # Validations
  # ----------------------------------------------------------------------
  validates :first_name, presence: true
  validates :last_name, presence: true

  # ----------------------------------------------------------------------
  # Methods
  # ----------------------------------------------------------------------
  def full_name
    "#{first_name} #{last_name}"
  end
end
